package model;

public class InicioFim {
	
	String numAtendimento;
	String numPlano;
	String numAquisicao;
	
	
	public String getNumAtendimento() {
		return numAtendimento;
	}
	public void setNumAtendimento(String numAtendimento) {
		this.numAtendimento = numAtendimento;
	}
	public String getNumPlano() {
		return numPlano;
	}
	public void setNumPlano(String numPlano) {
		this.numPlano = numPlano;
	}
	public String getNumAquisicao() {
		return numAquisicao;
	}
	public void setNumAquisicao(String numAquisicao) {
		this.numAquisicao = numAquisicao;
	}
}
