package modelResponse;

public class PendenciaResponse {
	
	private int midia;
	private String aceiteDigital;
	private int qtdPrestacao;
	
	
	
	public int getMidia() {
		return midia;
	}
	
	public void setMidia(int midia) {
		this.midia = midia;
	}
	
	public String getAceiteDigital() {
		return aceiteDigital;
	}
	
	public void setAceiteDigital(String aceiteDigital) {
		this.aceiteDigital = aceiteDigital;
	}
	
	public int getQtdPrestacao() {
		return qtdPrestacao;
	}
	
	public void setQtdPrestacao(int qtdPrestacao) {
		this.qtdPrestacao = qtdPrestacao;
	}
}
