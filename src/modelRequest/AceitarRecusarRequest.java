package modelRequest;

public class AceitarRecusarRequest {

	int midia;
	String solicitacaoUsuario;		
	int numProposta;	
	int numPlano;
	int codSubscritor;
	String codAceite;
	String indAceite;
	
	
	public int getMidia() {
		return midia;
	}
	
	public void setMidia(int midia) {
		this.midia = midia;
	}
	
	public String getSolicitacaoUsuario() {
		return solicitacaoUsuario;
	}
	
	public void setSolicitacaoUsuario(String solicitacaoUsuario) {
		this.solicitacaoUsuario = solicitacaoUsuario;
	}
	
	public int getNumProposta() {
		return numProposta;
	}
	
	public void setNumProposta(int numProposta) {
		this.numProposta = numProposta;
	}
	
	public int getNumPlano() {
		return numPlano;
	}
	
	public void setNumPlano(int numPlano) {
		this.numPlano = numPlano;
	}
	
	public int getCodSubscritor() {
		return codSubscritor;
	}
	
	public void setCodSubscritor(int codSubscritor) {
		this.codSubscritor = codSubscritor;
	}
	
	public String getCodAceite() {
		return codAceite;
	}
	
	public void setCodAceite(String codAceite) {
		this.codAceite = codAceite;
	}
	
	public String getIndAceite() {
		return indAceite;
	}
	
	public void setIndAceite(String indAceite) {
		this.indAceite = indAceite;
	}
		
}
